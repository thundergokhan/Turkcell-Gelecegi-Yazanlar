
public class Driver {

	public static void main(String[] args) {
		byte b; // değişkeni tanımladık 1byte
		b = 10; // değişkenin değerini atadık
		System.out.println(b);

		short s = 20; // bellekte 2 byte yer kaplamaktadır
		System.out.println(s);

		int i = 30; // 4 byte
		System.out.println(i);

		long l = 40; // 8 byte
		System.out.println(l);

		float pi = 3.14f; // 4 byte
		System.out.println(pi);

		double d = 23.32232323; // 8 byte
		System.out.println(d);

		char c = 'a'; // 2 byte
		System.out.println(c);

		boolean bool = true;
		System.out.println(bool);

		// int $abc = 10;

		// int pi = 312;

		int yas = 34;
		System.out.println(yas);
	}
}
